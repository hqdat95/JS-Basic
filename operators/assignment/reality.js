let x = 8; // Assign number 8 to x
let y = 2; // Assign number 2 to y

// Addition
x += y; // Equivalent to: x = x + y
console.log('Addition: ', x); // Output: 10

// Subtract
x -= y; // Equivalent to: x = x - y
console.log('Subtract:', x); // Output: 6

// Multiply
x *= y; // Equivalent to: x = x * y
console.log('Multiply:', x); // Output: 16

// Divide
x /= y; // Equivalent to: x = x / y
console.log('Divide:', x); // Output: 4

// Remainder
x %= y; // Equivalent to: x = x % y
console.log('Remainder:', x); // Output: 0

// Exponential
x **= y; // Equivalent to: x = x ** y
console.log('Exponential:', x); // Output: 64
