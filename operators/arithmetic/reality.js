let a = 2;
let b = 4;

// Addition
console.log('Addition:', a + b); // Output: 6

// Subtract
console.log('Subtract:', a - b); // Output: -2

// Multiply
console.log('Multiply:', a * b); // Output: 8

// Divide
console.log('Divide:', a / b); // Output: 0.5

// Exponential
console.log('Exponential:', a ** b); // Output: 16

// Remainder
console.log('Remainder:', a % b); // Output: 2

// Increment by 1
console.log('Increment by 1:', a++); // Output: 3

// Decrement by 1
console.log('Decrement by 1:', a--); // Output: 1
